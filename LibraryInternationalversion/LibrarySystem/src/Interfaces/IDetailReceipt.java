    private void okbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_okbtnMouseClicked
        // TODO add your handling code here:
           
            if (this.fbcyeartxt.getText().length() == 0)  
            {
                JOptionPane.showMessageDialog(null, "PLEASE FILL ALL FIELDS", "WARNING", 1);
                return;
            }
                     
            else 
            {    
                
            }
                
                
                  try
                    {
                      this.drtbl.InsertReceipttoSQL(jComboBox1.getSelectedItem().toString(), jComboBox2.getSelectedItem().toString(), fbcyeartxt.getText(), Integer.parseInt(this.costtxt.getText()), this.convertJavaDateToSqlDate(this.jDateChooser1.getDate()));
                      JOptionPane.showMessageDialog(null, "ADD SUCCESSFULLY!", "WARNING",  1);
                      DetailReceiptscrn dr = new DetailReceiptscrn();
                      dr.ClearData();
                      dr.ShowData();
                      dr.setVisible(true);
                      this.dispose();
                    }
                  catch (SQLException e)
                    {       
                     JOptionPane.showMessageDialog(null, "ERROR", "WARNING", 1);   
                    }
                  
        
    }//GEN-LAST:event_okbtnMouseClicked

