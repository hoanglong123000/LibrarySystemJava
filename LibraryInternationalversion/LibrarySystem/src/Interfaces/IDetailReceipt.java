    public void Fillfullnamebox()
    {
        try
        {
            cn.connectSQL();
            String sql = "SELECT Fullname FROM Reader";
            PreparedStatement st = cn.conn.prepareStatement(sql);
            ResultSet ref = st.executeQuery();
            while(ref.next())
            {
                String fullname = ref.getNString("Fullname");
                jComboBox1.addItem(fullname);
            }
            
        }
        catch (Exception e)
        {
            
        }
    }
    
    public void Fillididentblbox()
    {
        try
        {
            cn.connectSQL();
            String sql = "SELECT ididencard FROM Identitycard";
            PreparedStatement st = cn.conn.prepareStatement(sql);
            ResultSet ref = st.executeQuery();
            while(ref.next())
            {
                String iddr = ref.getString("ididencard");
                jComboBox2.addItem(iddr);
            }
            
        }
        catch (Exception e)
        {
            
        }
    }
    
    
    
