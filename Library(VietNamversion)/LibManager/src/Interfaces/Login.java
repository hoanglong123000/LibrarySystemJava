    private void signinbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_signinbtnMouseClicked
        try {
            // TODO add your handling code here:
            this.cn.connectSQL();
            String sql = "SELECT * FROM Employee WHERE Name=? AND gender=? AND jobtitle=? AND age=? AND email=?";
           
            PreparedStatement pst = cn.conn.prepareStatement(sql);
            pst.setString(1, this.fullnametxt.getText());
            pst.setBoolean(2, this.malechck.isSelected());
            pst.setString(3, this.jobtitletxt.getText());
            pst.setInt(4, Integer.parseInt(this.agetxt.getText()));
            pst.setString(5, this.emailtxt.getText());
            ResultSet r = pst.executeQuery();
            
            if (r.next())
            {     
                JOptionPane.showMessageDialog(null, "Đăng nhập thành công!", "Thông báo", 1);
                l.setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Vui lòng kiểm tra họ tên, chức vụ, email của bạn", "Thông báo", 1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_signinbtnMouseClicked

